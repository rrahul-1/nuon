// Command-line flags
var (
	outputDir = flag.String("output", "docs/config-ref", "Output directory for generated markdown files")
	format    = flag.String("format", "mintlify", "Output format: mintlify, github, or plain")
	verbose   = flag.Bool("verbose", false, "Enable verbose logging")
)

const generatorDisclaimer = "{/* @generated Code generated by pkg/gen/markdown-docs/main.go. DO NOT EDIT. */}\n\n"

func main() {
	flag.Parse()

	startTime := time.Now()
	log.Printf("Generating config reference documentation to %s (format: %s)", *outputDir, *format)

	// Create output directory
	if err := os.MkdirAll(*outputDir, 0o755); err != nil {
		log.Fatalf("Failed to create output directory: %v", err)
	}

	// Get sorted schema names
	var schemaNames []string
	for name := range schema.SchemaMapping {
		schemaNames = append(schemaNames, name)
	}
	sort.Strings(schemaNames)
	log.Printf("Found %d schemas to document", len(schemaNames))

	// Generate documentation for each schema
	for _, name := range schemaNames {
		if err := generateSchemaDoc(name, *outputDir, *format); err != nil {
			log.Fatalf("Failed to generate docs for %s: %v", name, err)
		}
	}

	// Generate index page
	if err := generateIndexPage(schemaNames, *outputDir, *format); err != nil {
		log.Fatalf("Failed to generate index page: %v", err)
	}

	duration := time.Since(startTime)
	log.Printf("✓ Successfully generated config reference documentation in %s (took %s)", *outputDir, duration)
}

func generateSchemaDoc(schemaName, outputDir, format string) error {
	ignoredOneOffs := []string{
		"component_type",
		"docker_build",
		"external_image",
		"helm_chart",
		"job",
		"kubernetes_manifest",
		"terraform_module",
	}
	log.Printf("[%s] Generating documentation...", schemaName)

	// Lookup the schema using the same method as the API endpoint
	s, err := schema.LookupSchemaType(schemaName)
	if err != nil {
		return fmt.Errorf("failed to lookup schema: %w", err)
	}
	if s == nil {
		return fmt.Errorf("schema not found")
	}

	// Create markdown AST document
	doc := mdast.NewDocument()

	// Add frontmatter for Mintlify
	if format == "mintlify" {
		doc.AddFrontmatter(map[string]string{
			"title":       formatTitle(schemaName),
			"description": fmt.Sprintf("JSON Schema reference for %s configuration", schemaName),
		})
	}

	doc.AddRaw(generatorDisclaimer)

	// Add title
	doc.AddHeading(1, formatTitle(schemaName))

	// Find the target schema (handle $ref schemas like the API does)
	targetSchema := resolveSchemaRef(s)

	// Collect all definitions from the original schema for sub-type expansion
	allDefs := collectDefinitions(s)

	// Add properties table if schema has properties
	if targetSchema.Properties != nil && targetSchema.Properties.Len() > 0 {
		doc.AddHeading(2, "Properties")

		// Collect properties, partitioned into required and optional
		type propEntry struct {
			name     string
			prop     *jsonschema.Schema
			required bool
		}
		var requiredProps, optionalProps []propEntry

		for pair := targetSchema.Properties.Oldest(); pair != nil; pair = pair.Next() {
			propName := pair.Key
			prop := pair.Value

			// Skip excluded properties
			if isExcludedProperty(propName) {
				continue
			}

			// Skip component type properties (oneof_required=component_type)
			if prop.Extras != nil {
				if oneOfReq, ok := prop.Extras["oneof_required"].(string); ok && slices.Contains(ignoredOneOffs, oneOfReq) {
					continue
				}
			}

			isRequired := isPropertyRequired(targetSchema.Required, propName)
			entry := propEntry{name: propName, prop: prop, required: isRequired}
			if isRequired {
				requiredProps = append(requiredProps, entry)
			} else {
				optionalProps = append(optionalProps, entry)
			}
		}

		allEntries := append(requiredProps, optionalProps...)

		// Find properties that reference sub-schemas (compute before table so we can link)
		type subSchemaRef struct {
			propName string
			def      *jsonschema.Schema
		}
		var subSchemaRefs []subSchemaRef
		subSchemaPropNames := make(map[string]bool)
		seenDefs := make(map[string]bool)

		for _, entry := range allEntries {
			defName := getPropertyRefName(entry.prop)
			if defName == "" || seenDefs[defName] {
				continue
			}
			if def, ok := allDefs[defName]; ok && def.Properties != nil && def.Properties.Len() > 0 {
				subSchemaRefs = append(subSchemaRefs, subSchemaRef{
					propName: entry.name,
					def:      def,
				})
				subSchemaPropNames[entry.name] = true
				seenDefs[defName] = true
			}
		}

		// Build properties table with required fields first
		table := mdast.NewTable([]string{
			"Property",
			"Description",
			"Values",
			"Example",
		})

		for _, entry := range allEntries {
			table.AddRow(buildPropertyRow(entry.name, entry.prop, entry.required, subSchemaPropNames))
		}

		doc.AddTable(table)

		// Render sub-type sections
		for _, ref := range subSchemaRefs {
			doc.AddHeading(3, mdast.Code(ref.propName))
			renderSubSchemaTable(doc, ref.def)
		}
	}

	// Render markdown
	markdown := doc.Render()

	// Write to file
	filename := filepath.Join(outputDir, schemaName+".mdx")
	if err := os.WriteFile(filename, []byte(markdown), 0o644); err != nil {
		return fmt.Errorf("failed to write file: %w", err)
	}

	log.Printf("[%s] ✓ Written to %s", schemaName, filename)
	return nil
}

func generateIndexPage(schemaNames []string, outputDir, format string) error {
	log.Printf("Generating index page...")
	doc := mdast.NewDocument()

	// Add frontmatter
	if format == "mintlify" {
		doc.AddFrontmatter(map[string]string{
			"title":       "Configuration Reference",
			"description": "Complete reference for Nuon configuration file schemas",
		})
	}

	doc.AddRaw(generatorDisclaimer)

	doc.AddHeading(1, "Configuration Reference")
	doc.AddParagraph("This section provides detailed JSON Schema references for all Nuon configuration types.")
	doc.AddHeading(2, "Available Configurations")

	if format == "mintlify" {
		doc.AddRaw("<CardGroup cols={2}>\n")
		for _, name := range schemaNames {
			doc.AddRaw(fmt.Sprintf("  <Card title=\"%s\" icon=\"file\" href=\"%s\"></Card>\n",
				formatTitle(name), name))
		}
		doc.AddRaw("</CardGroup>\n\n")
	} else {
		for _, name := range schemaNames {
			doc.AddListItem(fmt.Sprintf("[%s](%s.mdx)", formatTitle(name), name))
		}
	}

	// Write index file
	filename := filepath.Join(outputDir, "index.mdx")
	if err := os.WriteFile(filename, []byte(doc.Render()), 0o644); err != nil {
		return fmt.Errorf("failed to write index file: %w", err)
	}

	log.Printf("✓ Index page written to %s", filename)
	return nil
}

// resolveSchemaRef handles $ref schemas and allOf compositions
func resolveSchemaRef(s *jsonschema.Schema) *jsonschema.Schema {
	// Handle allOf first
	if len(s.AllOf) > 0 {
		return mergeAllOfSchemas(s, s.Definitions)
	}

	// Then handle $ref
	if s.Ref != "" && s.Definitions != nil {
		refParts := strings.Split(s.Ref, "/")
		if len(refParts) > 0 {
			defName := refParts[len(refParts)-1]
			if def, ok := s.Definitions[defName]; ok {
				// After resolving ref, check if it has allOf
				if len(def.AllOf) > 0 {
					return mergeAllOfSchemas(def, s.Definitions)
				}
				return def
			}
		}
	}

	return s
}

// mergeAllOfSchemas merges all schemas in an allOf array into a single schema
func mergeAllOfSchemas(s *jsonschema.Schema, definitions map[string]*jsonschema.Schema) *jsonschema.Schema {
	// Create a new schema to hold merged properties
	merged := &jsonschema.Schema{
		Type:        s.Type,
		Description: s.Description,
		Properties:  jsonschema.NewProperties(),
		Required:    []string{},
	}

	// Copy properties from the parent schema if any
	if s.Properties != nil && s.Properties.Len() > 0 {
		for pair := s.Properties.Oldest(); pair != nil; pair = pair.Next() {
			merged.Properties.Set(pair.Key, pair.Value)
		}
	}

	// Copy required fields from parent
	merged.Required = append(merged.Required, s.Required...)

	// Merge properties and required fields from all allOf schemas
	for _, allOfSchema := range s.AllOf {
		// Resolve any $ref in the allOf entry
		// Use the allOfSchema's own Definitions for resolution
		resolved := allOfSchema
		if allOfSchema.Ref != "" {
			// Try to resolve from the allOfSchema's own definitions first
			if allOfSchema.Definitions != nil {
				refParts := strings.Split(allOfSchema.Ref, "/")
				if len(refParts) > 0 {
					defName := refParts[len(refParts)-1]
					if def, ok := allOfSchema.Definitions[defName]; ok {
						resolved = def
					}
				}
			}
			// Fallback to parent definitions if provided
			if resolved == allOfSchema && definitions != nil {
				refParts := strings.Split(allOfSchema.Ref, "/")
				if len(refParts) > 0 {
					defName := refParts[len(refParts)-1]
					if def, ok := definitions[defName]; ok {
						resolved = def
					}
				}
			}
		}

		// Recursively handle nested allOf
		if len(resolved.AllOf) > 0 {
			resolved = mergeAllOfSchemas(resolved, definitions)
		}

		// Merge properties
		if resolved.Properties != nil && resolved.Properties.Len() > 0 {
			for pair := resolved.Properties.Oldest(); pair != nil; pair = pair.Next() {
				merged.Properties.Set(pair.Key, pair.Value)
			}
		}

		// Merge required fields
		merged.Required = append(merged.Required, resolved.Required...)

		// Use first non-empty description
		if merged.Description == "" && resolved.Description != "" {
			merged.Description = resolved.Description
		}

		// Use first non-empty type
		if merged.Type == "" && resolved.Type != "" {
			merged.Type = resolved.Type
		}
	}

	return merged
}

func isPropertyRequired(required []string, propName string) bool {
	for _, req := range required {
		if req == propName {
			return true
		}
	}
	return false
}

// isExcludedProperty checks if a property should be excluded from documentation
func isExcludedProperty(propName string) bool {
	// Properties that are common across schemas but don't need documentation
	excludedProperties := []string{
		"source",
	}

	for _, excluded := range excludedProperties {
		if propName == excluded {
			return true
		}
	}
	return false
}

// buildPropertyRow creates a table row for a single property.
// subSchemaPropNames indicates which property names have sub-sections below (used to render links).
func buildPropertyRow(name string, prop *jsonschema.Schema, required bool, subSchemaPropNames map[string]bool) []string {
	typeStr := getTypeString(prop)
	if subSchemaPropNames[name] {
		typeStr = "[" + typeStr + "](#" + name + ")"
	}
	propertyCell := "**" + mdast.Code(name) + "**" +
		"<br/>" + typeStr

	valuesParts := []string{"**" + formatRequired(required) + "**"}
	if d := formatDefault(prop.Default); d != "-" {
		valuesParts = append(valuesParts, "Default: "+d)
	}
	if len(prop.Enum) > 0 {
		valuesParts = append(valuesParts, formatEnum(prop.Enum))
	}

	return []string{
		propertyCell,
		formatDescription(prop.Description),
		strings.Join(valuesParts, "<br/>"),
		formatExample(prop),
	}
}

// renderSubSchemaTable renders a properties table for a sub-type definition
func renderSubSchemaTable(doc *mdast.Document, schema *jsonschema.Schema) {
	if schema.Properties == nil || schema.Properties.Len() == 0 {
		return
	}

	table := mdast.NewTable([]string{"Property", "Description", "Values", "Example"})

	type propEntry struct {
		name     string
		prop     *jsonschema.Schema
		required bool
	}
	var requiredProps, optionalProps []propEntry

	for pair := schema.Properties.Oldest(); pair != nil; pair = pair.Next() {
		isRequired := isPropertyRequired(schema.Required, pair.Key)
		entry := propEntry{name: pair.Key, prop: pair.Value, required: isRequired}
		if isRequired {
			requiredProps = append(requiredProps, entry)
		} else {
			optionalProps = append(optionalProps, entry)
		}
	}

	for _, entry := range append(requiredProps, optionalProps...) {
		table.AddRow(buildPropertyRow(entry.name, entry.prop, entry.required, nil))
	}

	doc.AddTable(table)
}

// collectDefinitions gathers all definitions from a schema and its allOf sub-schemas
func collectDefinitions(s *jsonschema.Schema) map[string]*jsonschema.Schema {
	defs := make(map[string]*jsonschema.Schema)

	if s.Definitions != nil {
		for name, def := range s.Definitions {
			defs[name] = def
		}
	}

	for _, allOfSchema := range s.AllOf {
		if allOfSchema.Definitions != nil {
			for name, def := range allOfSchema.Definitions {
				defs[name] = def
			}
		}
	}

	return defs
}

// getPropertyRefName extracts the definition name referenced by a property (direct $ref or array items $ref)
func getPropertyRefName(prop *jsonschema.Schema) string {
	if prop.Ref != "" {
		parts := strings.Split(prop.Ref, "/")
		return parts[len(parts)-1]
	}
	if prop.Items != nil && prop.Items.Ref != "" {
		parts := strings.Split(prop.Items.Ref, "/")
		return parts[len(parts)-1]
	}
	return ""
}

func getTypeString(prop *jsonschema.Schema) string {
	if prop.Type != "" {
		return prop.Type
	}

	// Handle array types
	if prop.Items != nil {
		itemType := getTypeString(prop.Items)
		return "array<" + itemType + ">"
	}

	// Handle refs
	if prop.Ref != "" {
		parts := strings.Split(prop.Ref, "/")
		if len(parts) > 0 {
			return parts[len(parts)-1]
		}
	}

	return "object"
}

func formatRequired(required bool) string {
	if required {
		return "✅ Required"
	}
	return "Optional"
}

func formatDescription(desc string) string {
	if desc == "" {
		return "-"
	}

	// Replace newlines with spaces for table format
	desc = strings.ReplaceAll(desc, "\n", " ")

	// Escape special MDX characters
	desc = mdast.EscapeMDX(desc)

	// Limit length if too long
	if len(desc) > 200 {
		runes := []rune(desc)
		if len(runes) > 197 {
			desc = string(runes[:197]) + "..."
		}
	}
	return desc
}

func formatDefault(defaultVal any) string {
	if defaultVal == nil {
		return "-"
	}

	bytes, _ := json.Marshal(defaultVal)
	return mdast.Code(string(bytes))
}

func formatEnum(enum []any) string {
	var parts []string
	for _, val := range enum {
		bytes, _ := json.Marshal(val)
		parts = append(parts, mdast.Code(string(bytes)))
	}
	return strings.Join(parts, ", ")
}

func formatExample(prop *jsonschema.Schema) string {
	if len(prop.Examples) > 0 {
		var parts []string
		for _, example := range prop.Examples {
			bytes, _ := json.Marshal(example)
			parts = append(parts, mdast.Code(string(bytes)))
		}
		return strings.Join(parts, ", ")
	}

	return "-"
}

func formatTitle(schemaName string) string {
	words := strings.Split(schemaName, "-")
	caser := cases.Title(language.English)
	for i, word := range words {
		words[i] = caser.String(word)
	}
	return strings.Join(words, " ")
}
