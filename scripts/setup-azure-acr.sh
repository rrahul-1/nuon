#!/bin/bash
#
# Set up an Azure Container Registry (ACR) for local ctl-api development.
#
# This script creates (or reuses) an ACR in a given resource group, then
# prints the environment variables needed to run ctl-api with
# CLOUD_PROVIDER=azure so that builds push to ACR instead of AWS ECR.
#
# Prerequisites:
#   - Azure CLI (`az`) installed and logged in
#   - A resource group to create the ACR in
#
# Usage:
#   ./scripts/setup-azure-acr.sh                     # interactive prompts
#   ./scripts/setup-azure-acr.sh --rg mygroup        # specify resource group
#   ./scripts/setup-azure-acr.sh --rg mygroup --name myacr --location eastus
#
# After running, source the generated env file:
#   source /tmp/nuon-azure-acr.env
#

set -euo pipefail

# ── parse args ────────────────────────────────────────────────────────
RESOURCE_GROUP=""
ACR_NAME=""
LOCATION=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --rg)          RESOURCE_GROUP="$2"; shift 2 ;;
    --name)        ACR_NAME="$2"; shift 2 ;;
    --location)    LOCATION="$2"; shift 2 ;;
    -h|--help)
      echo "Usage: $0 [--rg RESOURCE_GROUP] [--name ACR_NAME] [--location LOCATION]"
      exit 0
      ;;
    *) echo "Unknown option: $1"; exit 1 ;;
  esac
done

# ── check prerequisites ──────────────────────────────────────────────
if ! command -v az &>/dev/null; then
  echo "❌ Azure CLI (az) is not installed. Install it first:"
  echo "   brew install azure-cli"
  exit 1
fi

if ! az account show &>/dev/null; then
  echo "❌ Not logged in to Azure. Run: az login"
  exit 1
fi

ACCOUNT_INFO=$(az account show -o json)
SUBSCRIPTION_ID=$(echo "$ACCOUNT_INFO" | jq -r '.id')
TENANT_ID=$(echo "$ACCOUNT_INFO" | jq -r '.tenantId')
ACCOUNT_NAME=$(echo "$ACCOUNT_INFO" | jq -r '.name')

echo "Azure account: ${ACCOUNT_NAME}"
echo "Subscription:  ${SUBSCRIPTION_ID}"
echo "Tenant:        ${TENANT_ID}"
echo ""

# ── resolve resource group ───────────────────────────────────────────
if [ -z "$RESOURCE_GROUP" ]; then
  echo "Available resource groups:"
  az group list --query "[].name" -o tsv | head -20
  echo ""
  read -rp "Resource group (or enter a new name to create): " RESOURCE_GROUP
fi

if ! az group show --name "$RESOURCE_GROUP" &>/dev/null; then
  if [ -z "$LOCATION" ]; then
    read -rp "Resource group doesn't exist. Location to create it in (e.g. eastus): " LOCATION
  fi
  echo "==> Creating resource group ${RESOURCE_GROUP} in ${LOCATION}..."
  az group create --name "$RESOURCE_GROUP" --location "$LOCATION" -o none
else
  if [ -z "$LOCATION" ]; then
    LOCATION=$(az group show --name "$RESOURCE_GROUP" --query location -o tsv)
  fi
fi

echo "Resource group: ${RESOURCE_GROUP} (${LOCATION})"

# ── resolve ACR name ─────────────────────────────────────────────────
if [ -z "$ACR_NAME" ]; then
  # Check for existing ACR in the resource group
  EXISTING_ACR=$(az acr list --resource-group "$RESOURCE_GROUP" \
    --query "[0].name" -o tsv 2>/dev/null || true)

  if [ -n "$EXISTING_ACR" ] && [ "$EXISTING_ACR" != "null" ]; then
    echo "Found existing ACR: ${EXISTING_ACR}"
    read -rp "Use it? [Y/n]: " USE_EXISTING
    if [[ "${USE_EXISTING:-Y}" =~ ^[Yy]?$ ]]; then
      ACR_NAME="$EXISTING_ACR"
    fi
  fi

  if [ -z "$ACR_NAME" ]; then
    DEFAULT_NAME="nuondev$(whoami | tr -cd '[:alnum:]' | head -c 6)"
    read -rp "ACR name (alphanumeric only) [${DEFAULT_NAME}]: " ACR_NAME
    ACR_NAME="${ACR_NAME:-$DEFAULT_NAME}"
  fi
fi

# ── create or reuse ACR ──────────────────────────────────────────────
if az acr show --name "$ACR_NAME" --resource-group "$RESOURCE_GROUP" &>/dev/null; then
  echo "==> Using existing ACR: ${ACR_NAME}"
else
  echo "==> Creating ACR: ${ACR_NAME} (Basic SKU)..."
  az acr create \
    --name "$ACR_NAME" \
    --resource-group "$RESOURCE_GROUP" \
    --location "$LOCATION" \
    --sku Basic \
    --admin-enabled false \
    -o none
  echo "   ACR created."
fi

# ── get ACR details ──────────────────────────────────────────────────
ACR_LOGIN_SERVER=$(az acr show --name "$ACR_NAME" --resource-group "$RESOURCE_GROUP" \
  --query "loginServer" -o tsv)

echo ""
echo "ACR login server: ${ACR_LOGIN_SERVER}"

# ── get current user's client ID (for managed identity / local dev) ──
# When running locally, Azure SDK uses your az cli credentials.
# The "client ID" for local dev is your az cli app registration.
# For ctl-api, ManagementAzureClientID is used for runner IAM provisioning.
# Locally you can set it to your SP or leave empty if not provisioning runners.
CLIENT_ID=$(echo "$ACCOUNT_INFO" | jq -r '.user.name // empty')

# ── write env file ───────────────────────────────────────────────────
ENV_FILE="/tmp/nuon-azure-acr.env"

cat > "$ENV_FILE" << EOF
# Azure ACR management registry config for ctl-api local development
# Generated by setup-azure-acr.sh on $(date -u +%Y-%m-%dT%H:%M:%SZ)
#
# Source this file before starting ctl-api:
#   source ${ENV_FILE}

# Core: switches all registry logic to Azure ACR
export CLOUD_PROVIDER=azure

# Management ACR registry
export MANAGEMENT_ACR_REGISTRY_URL=${ACR_LOGIN_SERVER}
export MANAGEMENT_AZURE_TENANT_ID=${TENANT_ID}
export MANAGEMENT_AZURE_CLIENT_ID=${CLIENT_ID}

# Management account ID (Azure subscription ID)
export MANAGEMENT_ACCOUNT_ID=${SUBSCRIPTION_ID}

# App region (Azure location)
export APP_REGION=${LOCATION}
EOF

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "✅ Azure ACR is ready!"
echo ""
echo "Environment file written to: ${ENV_FILE}"
echo ""
cat "$ENV_FILE" | grep "^export"
echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo "To use:"
echo "  source ${ENV_FILE}"
echo "  go run services/ctl-api/main.go api"
echo ""
echo "To verify ACR auth works:"
echo "  az acr login --name ${ACR_NAME}"
echo ""
