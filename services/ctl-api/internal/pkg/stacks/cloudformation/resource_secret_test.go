func TestGetSecretsParameters_OptionalSecretsDefaultToEmptyString(t *testing.T) {
	tpl := &Templates{}
	inp := &stacks.TemplateInput{
		AppCfg: &app.AppConfig{
			SecretsConfig: app.AppSecretsConfig{
				Secrets: []app.AppSecretConfig{
					{
						Name:                    "required-secret",
						CloudFormationParamName: "RequiredSecretParam",
						Required:                true,
					},
					{
						Name:                    "optional-secret",
						CloudFormationParamName: "OptionalSecretParam",
						Required:                false,
					},
				},
			},
		},
	}

	params := tpl.getSecretsParameters(inp)

	required, ok := params["RequiredSecretParam"]
	require.True(t, ok)
	assert.Nil(t, required.Default)
	assert.NotNil(t, required.AllowedPattern)

	optional, ok := params["OptionalSecretParam"]
	require.True(t, ok)
	assert.NotNil(t, optional.Default)
	optionalDefault, ok := optional.Default.(*string)
	require.True(t, ok)
	assert.Equal(t, "", *optionalDefault)
	assert.Nil(t, optional.AllowedPattern)
}

func TestGetSecretsConditions_OnlyIncludesOptionalNonAutogeneratedSecrets(t *testing.T) {
	tpl := &Templates{}
	inp := &stacks.TemplateInput{
		AppCfg: &app.AppConfig{
			SecretsConfig: app.AppSecretsConfig{
				Secrets: []app.AppSecretConfig{
					{
						CloudFormationParamName: "RequiredSecretParam",
						Required:                true,
					},
					{
						CloudFormationParamName: "OptionalSecretParam",
						Required:                false,
					},
					{
						CloudFormationParamName: "AutogeneratedSecretParam",
						AutoGenerate:            true,
					},
				},
			},
		},
	}

	conditions := tpl.getSecretsConditions(inp)

	assert.Len(t, conditions, 1)
	assert.Contains(t, conditions, "OptionalSecretParamProvided")
}

func TestGetSecretsResources_OptionalSecretGetsCloudFormationCondition(t *testing.T) {
	tpl := &Templates{}
	inp := &stacks.TemplateInput{
		AppCfg: &app.AppConfig{
			SecretsConfig: app.AppSecretsConfig{
				Secrets: []app.AppSecretConfig{
					{
						Name:                    "required-secret",
						CloudFormationStackName: "RequiredSecret",
						CloudFormationParamName: "RequiredSecretParam",
						Required:                true,
					},
					{
						Name:                    "optional-secret",
						CloudFormationStackName: "OptionalSecret",
						CloudFormationParamName: "OptionalSecretParam",
						Required:                false,
					},
				},
			},
		},
	}

	resources := tpl.getSecretsResources(inp, tagBuilder{installID: "inl_test"})

	requiredResource, ok := resources["RequiredSecret"]
	require.True(t, ok)
	requiredSecret, ok := requiredResource.(*secretsmanager.Secret)
	require.True(t, ok)
	assert.Equal(t, "", requiredSecret.AWSCloudFormationCondition)

	optionalResource, ok := resources["OptionalSecret"]
	require.True(t, ok)
	optionalSecret, ok := optionalResource.(*secretsmanager.Secret)
	require.True(t, ok)
	assert.Equal(t, "OptionalSecretParamProvided", optionalSecret.AWSCloudFormationCondition)
}
